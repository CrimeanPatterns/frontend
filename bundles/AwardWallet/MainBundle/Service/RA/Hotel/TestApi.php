    protected function call(string $method, string $path, ?string $data = null): \HttpDriverResponse
    {
        if ($path === '/v1/search') {
            $query = json_decode($data, true);
            $query['userData'] = $userData = json_decode($query['userData'], true);
            $requestId = $userData['cacheKey'];

            $this->saveInCache(
                $requestId,
                [
                    'query' => $query,
                    'resultTs' => time() + random_int(15, 60),
                ]
            );

            return new \HttpDriverResponse(
                json_encode([
                    'requestId' => $userData['cacheKey'],
                ]),
                200
            );
        } elseif (preg_match('/^\/v1\/getResults\/(?<requestId>\w+)$/', $path, $matches)) {
            $requestId = $matches['requestId'];
            $cached = $this->getFromCache($requestId);

            if (!$cached) {
                return new \HttpDriverResponse(
                    json_encode([
                        'requestId' => $requestId,
                        'state' => 'queued_up',
                    ]),
                    200
                );
            }

            $query = $cached['query'];
            $resultTs = $cached['resultTs'];

            if (time() >= $resultTs) {
                return new \HttpDriverResponse(
                    json_encode([
                        'requestId' => $requestId,
                        'state' => 'success',
                        'message' => 'Success',
                        'requestDate' => date('c'),
                        'userData' => json_encode($query['userData']),
                        'hotels' => $this->getRandomHotel($query['userData']['provider']),
                    ]),
                    200
                );
            } else {
                return new \HttpDriverResponse(
                    json_encode([
                        'requestId' => $requestId,
                        'state' => 'queued_up',
                    ]),
                    200
                );
            }
        } elseif ($path === '/v1/providers/list') {
            return new \HttpDriverResponse(
                json_encode([
                    'providers' => [
                        [
                            'code' => 'goldpassport',
                            'displayName' => 'Hyatt (World of Hyatt)',
                            'shortName' => 'Hyatt',
                        ],
                        [
                            'code' => 'marriott',
                            'displayName' => 'Marriott Bonvoy',
                            'shortName' => 'Marriott',
                        ],
                        [
                            'code' => 'ichotelsgroup',
                            'displayName' => 'IHG Hotels & Resorts (One Rewards)',
                            'shortName' => 'IHG',
                        ],
                        [
                            'code' => 'hhonors',
                            'displayName' => 'Hilton (Honors)',
                            'shortName' => 'Hilton',
                        ],
                    ],
                ]),
                200
            );
        }

        return parent::call($method, $path, $data);
    }

    private function saveInCache(string $requestId, array $data): void
    {
        $this->memcached->set(
            sprintf('test_hotel_search_%s', $requestId),
            $data,
            60 * 60
        );
    }

    private function getFromCache(string $requestId)
    {
        return $this->memcached->get(sprintf('test_hotel_search_%s', $requestId));
    }

